package chapter001Exercises;

/* Write a program that displays the area and perimeter of a rectangle with width 4.5 and a height 7.9 using followinf formula:
    area = width x height
*/
public class Exercise1_9 {
    public static void main(String[] args) {
        System.out.println("Area      = " + (4.5 * 7.9));
        System.out.println("Perimeter = " + (2 * (4.5 + 7.9)));
    }
}
